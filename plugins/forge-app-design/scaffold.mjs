function resolveForgeRepo(explicitPath) {
  const candidates = [
    explicitPath,
    process.env.FORGE_REPO,
    process.env.FORGE_UI_REPO,
    findNearestForgeRepo(__dirname),
    findNearestForgeRepo(process.cwd()),
  ].filter(Boolean)

  for (const candidate of candidates) {
    const resolved = resolve(candidate)
    if (isForgeRepo(resolved)) return resolved
  }

  process.stderr.write([
    'Unable to locate the Forge repo root.',
    'Pass --forge-repo <path> or set FORGE_REPO to a directory containing core/package.json.',
  ].join('\n') + '\n')
  process.exit(2)
}

function findNearestForgeRepo(startDir) {
  let current = resolve(startDir)
  while (true) {
    if (isForgeRepo(current)) return current
    const parent = dirname(current)
    if (parent === current) return null
    current = parent
  }
}

function isForgeRepo(dir) {
  return existsSync(join(dir, 'core', 'package.json'))
}

