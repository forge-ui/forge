function domainProfileName(domain) {
  const text = String(domain ?? '').toLowerCase()
  if (/finance|billing|invoice|revenue/.test(text)) return 'Nora Lee'
  if (/support|ticket|service/.test(text)) return 'Owen Patel'
  if (/sales|crm|customer/.test(text)) return 'Priya Shah'
  if (/compliance|risk|policy|vendor/.test(text)) return 'Maya Chen'
  return 'Alex Morgan'
}

function escapeTsString(value) {
  return String(value ?? '').replaceAll('\\', '\\\\').replaceAll("'", "\\'")
}

function writeMockData(appIR, outputDir) {
  const mockDir = join(outputDir, 'mock')
  mkdirSync(mockDir, { recursive: true })

  for (const entity of appIR.entities) {
    const fileName = `${toKebabCase(entity.name)}.ts`
    writeFileSync(join(mockDir, fileName), mockEntityContent(entity), 'utf8')
  }

  writeFileSync(
    join(mockDir, 'index.ts'),
    appIR.entities.map(entity => `export * from './${toKebabCase(entity.name)}'`).join('\n') + '\n',
    'utf8',
  )
}

function mockEntityContent(entity) {
  const typeName = entity.name
  const crud = crudHelperName(entity)
  const fields = Array.isArray(entity.fields) ? entity.fields : []
  const rows = Array.from({ length: 12 }, (_item, index) => mockRow(entity, index))
  const fieldLines = fields.map(field => `  ${field.key}: ${fieldTsType(field)}`).join('\n')

  return `export interface ${typeName} {
  id: string
${fieldLines}
}

const initialRows: ${typeName}[] = ${JSON.stringify(rows, null, 2)}

let rows = [...initialRows]

export const ${crud} = {
  async list() {
    return rows
  },
  async get(id: string) {
    return rows.find(item => item.id === id) ?? null
  },
  async create(input: Partial<${typeName}>) {
    const item = { ...initialRows[0], ...input, id: input.id ?? \`${entityPrefix(entity.name)}-\${Date.now()}\` } as ${typeName}
    rows = [item, ...rows]
    return item
  },
  async update(id: string, input: Partial<${typeName}>) {
    let updated: ${typeName} | null = null
    rows = rows.map(item => {
      if (item.id !== id) return item
      updated = { ...item, ...input }
      return updated
    })
    return updated
  },
  async remove(id: string) {
    const before = rows.length
    rows = rows.filter(item => item.id !== id)
    return rows.length !== before
  },
}
`
}

function fieldTsType(field) {
  if (field?.type === 'number') return 'number'
  if (field?.type === 'boolean') return 'boolean'
  return 'string'
}

function mockRow(entity, index) {
  const row = { id: `${entityPrefix(entity.name)}-${String(1001 + index)}` }
  for (const field of entity.fields ?? []) {
    if (!field?.key) continue
    row[field.key] = mockValue(entity, field, index)
  }
  return row
}

function mockValue(entity, field, index) {
  const key = String(field.key)
  const lower = key.toLowerCase()
  const options = Array.isArray(field.options) && field.options.length ? field.options : null

  if (field.type === 'enum' && options) return options[index % options.length]
  if (field.type === 'number') {
    if (/risk|score|readiness|progress|percent|rate/.test(lower)) return [91, 78, 63, 52, 38, 84][index % 6]
    if (/amount|exposure|value|revenue|price|cost|total/.test(lower)) return [1180000, 740000, 510000, 185000, 96000, 420000][index % 6]
    return index + 1
  }
  if (field.type === 'boolean') return index % 2 === 0
  if (field.type === 'date') return `2026-06-${String(5 + (index % 20)).padStart(2, '0')}`
  if (field.type === 'ref') return `${entityPrefix(field.target ?? 'ref')}-${String(1001 + (index % 6))}`

  if (/name|title/.test(lower)) return `${displayLabel(entity.name)} ${index + 1}`
  if (/owner|assignee|reviewer|controller|manager/.test(lower)) return ['Maya Chen', 'Priya Shah', 'Daniel Kim', 'Elena Torres', 'Owen Patel', 'Nora Lee'][index % 6]
  if (/email/.test(lower)) return `user${index + 1}@example.com`
  if (/phone|mobile/.test(lower)) return `+1 415 555 ${String(1000 + index)}`
  if (/status/.test(lower)) return options ? options[index % options.length] : ['Active', 'Under Review', 'Blocked', 'Ready'][index % 4]
  if (/category|type|segment|tier/.test(lower)) return options ? options[index % options.length] : ['Enterprise', 'Mid-market', 'SMB', 'Internal'][index % 4]
  if (/reason|cause|summary|note|description|impact|action/.test(lower)) return [
    'Evidence packet requires owner review before release.',
    'Policy exception needs approval before the next workflow step.',
    'Finance exposure remains at risk until the blocker is cleared.',
    'Related records should be checked before escalation.',
  ][index % 4]

  return `${displayLabel(entity.name)} ${key} ${index + 1}`
}

function entityPrefix(name) {
  return String(name)
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .split(/[-_\s]+/)
    .filter(Boolean)
    .map(part => part[0])
    .join('')
    .toUpperCase()
    .slice(0, 4) || 'ID'
}

function routeToFsPath(route) {
  return String(route).replace(/^\//, '')
}

