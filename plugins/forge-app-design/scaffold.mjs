// ── Custom: in-memory CRUD API routes per entity ─────────────────────────────

/**
 * PascalCase → kebab-case, handling acronyms correctly.
 *   CustomerAccount → customer-account
 *   SLAPolicy       → sla-policy       (not s-l-a-policy)
 *   APIKey          → api-key
 * Keep API route names stable for generated mock CRUD helpers.
 */
function toKebabCase(str) {
  return String(str)
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1-$2')
    .toLowerCase()
    .replace(/[\s_]+/g, '-')
}

/** PascalCase → camelCase with acronym handling. */
function toCamelCase(str) {
  return String(str)
    .replace(/([A-Z]+)([A-Z][a-z])/g, (_m, p1, p2) => p1.charAt(0) + p1.slice(1).toLowerCase() + p2)
    .replace(/^([A-Z]+)$/, m => m.toLowerCase())
    .replace(/^(.)/, m => m.toLowerCase())
}

/** Small English pluralization helper for mock CRUD names. */
function pluralize(word) {
  if (/[^aeiouAEIOU]y$/.test(word)) return word.slice(0, -1) + 'ies'
  if (/(s|x|z|ch|sh)$/i.test(word)) return word + 'es'
  return word + 's'
}

function crudHelperName(entity) {
  // Export CRUD helpers as `pluralize(toCamelCase(entity.name))`:
  //   Customer    → customers
  //   Order       → orders
  //   SLAPolicy   → slaPolicies
  //   APIKey      → apiKeys
  return pluralize(toCamelCase(entity.name))
}

function apiCollectionRoute(entity) {
  const crud = crudHelperName(entity)
  return `// In-memory CRUD for ${entity.name} (collection)
import { NextResponse } from 'next/server'
import { ${crud} } from '@/mock'

export async function GET() {
  return NextResponse.json(await ${crud}.list())
}

export async function POST(req: Request) {
  const body = await req.json()
  const item = await ${crud}.create(body)
  return NextResponse.json(item, { status: 201 })
}
`
}

function apiItemRoute(entity) {
  const crud = crudHelperName(entity)
  // Next.js 16: params is Promise<{...}>, must await it.
  // CRUD helper uses .remove() (not .delete() — JS reserved word).
  return `// In-memory CRUD for ${entity.name} (single by id)
import { NextResponse } from 'next/server'
import { ${crud} } from '@/mock'

type RouteContext = { params: Promise<{ id: string }> }

export async function GET(_: Request, { params }: RouteContext) {
  const { id } = await params
  const item = await ${crud}.get(id)
  if (!item) return NextResponse.json({ error: 'not found' }, { status: 404 })
  return NextResponse.json(item)
}

export async function PATCH(req: Request, { params }: RouteContext) {
  const { id } = await params
  const body = await req.json()
  const item = await ${crud}.update(id, body)
  if (!item) return NextResponse.json({ error: 'not found' }, { status: 404 })
  return NextResponse.json(item)
}

export async function DELETE(_: Request, { params }: RouteContext) {
  const { id } = await params
  const ok = await ${crud}.remove(id)
  if (!ok) return NextResponse.json({ error: 'not found' }, { status: 404 })
  return NextResponse.json({ ok: true })
}
`
}

for (const entity of entities) {
  const kebab = toKebabCase(entity.name)
  const dir = join(outputDir, 'app/api', kebab)
  mkdirSync(dir, { recursive: true })
  writeFileSync(join(dir, 'route.ts'), apiCollectionRoute(entity), 'utf8')

  const dirItem = join(dir, '[id]')
  mkdirSync(dirItem, { recursive: true })
  writeFileSync(join(dirItem, 'route.ts'), apiItemRoute(entity), 'utf8')
}

