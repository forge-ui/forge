#!/usr/bin/env node
/**
 * forge-app-design scaffold tool
 *
 * 一键吐确定层：package.json / next config / api routes / mock data / app/layout
 * + 每个 page 的空 placeholder（等 LLM 后续 Write 业务拼装）。
 *
 * NOT does: 视觉拼装（broken composer 已废弃，由 LLM 直接看 forge cases/ 真示例自己写）。
 *
 * Usage:
 *   node scaffold.mjs \
 *     --name "<app-slug>" \
 *     --entities-json '[{"name":"Customer","fields":[...]},...]' \
 *     --pages-json '[{"id":"customer-list","route":"/customers","layout":"list-with-drawer","primaryEntity":"Customer","title":"客户管理"},...]' \
 *     [--domain "CRM"] [--output ./output/<name>] [--forge-repo <path>] [--with-auth]
 */

import { parseArgs } from 'node:util'
import { existsSync, mkdirSync, writeFileSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))

// ── CLI args ─────────────────────────────────────────────────────────────────

const { values } = parseArgs({
  options: {
    name: { type: 'string' },
    'entities-json': { type: 'string' },
    'pages-json': { type: 'string' },
    domain: { type: 'string', default: 'Other' },
    output: { type: 'string' },
    'forge-repo': { type: 'string' },
    'with-auth': { type: 'boolean', default: false },
  },
})

if (!values.name || !values['entities-json'] || !values['pages-json']) {
  process.stderr.write(
    'Usage: scaffold.mjs --name <slug> --entities-json <json> --pages-json <json> [--domain X] [--output ./output/X] [--forge-repo <path>] [--with-auth]\n',
  )
  process.exit(1)
}

let entities, pages
try {
  entities = JSON.parse(values['entities-json'])
  pages = JSON.parse(values['pages-json'])
} catch (err) {
  process.stderr.write(`Invalid JSON in --entities-json or --pages-json: ${err.message}\n`)
  process.exit(2)
}

if (!Array.isArray(entities) || entities.length === 0) {
  process.stderr.write('entities-json must be a non-empty array\n')
  process.exit(2)
}
if (!Array.isArray(pages) || pages.length === 0) {
  process.stderr.write('pages-json must be a non-empty array\n')
  process.exit(2)
}

const outputDir = values.output || `./output/${values.name}`
const forgeRepo = resolveForgeRepo(values['forge-repo'])

const appIR = {
  appName: values.name,
  appLabel: displayLabel(values.name),
  domain: values.domain,
  description: `${values.name}: generated by forge-app-design skill`,
  entities,
  pages,
  auth: {
    required: values['with-auth'],
    strategy: 'mock',
    loginRoute: '/login',
    defaultRedirect: pages[0]?.route ?? '/',
  },
  mockData: { rowsPerEntity: 20 },
}

function displayLabel(name) {
  const stop = new Set(['codex', 'sonnet', 'opus', 'spark', 'demo'])
  const words = String(name)
    .split(/[-_\s]+/)
    .filter(Boolean)
    .filter(word => !/^c\d+[a-z]?$/i.test(word))
    .filter(word => !/^v\d+$/i.test(word))
    .filter(word => !stop.has(word.toLowerCase()))

  const twoWordLabel = words
    .slice(0, 2)
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ')

  if (twoWordLabel && twoWordLabel.length <= 12) return twoWordLabel

  const firstWord = words[0]
    ? words[0].charAt(0).toUpperCase() + words[0].slice(1)
    : ''
  if (firstWord && firstWord.length <= 12) return firstWord

  const acronym = words.slice(0, 3).map(word => word.charAt(0).toUpperCase()).join('')
  return acronym || name
}

function isSidebarNavigationPage(page) {
  if (page?.surfaceRole) return page.surfaceRole === 'navigation'

  const route = String(page?.route ?? '')
  if (!route || route.includes('[') || route.includes('/:')) return false

  const segments = route.split('/').filter(Boolean)
  const leaf = segments.at(-1)?.toLowerCase()
  if (['new', 'create', 'edit', 'import', 'export', 'bulk'].includes(leaf)) return false

  return page?.layout !== 'wizard' && page?.routeRole !== 'wizard'
}

// ── Deterministic scaffold layer ─────────────────────────────────────────────

mkdirSync(outputDir, { recursive: true })

writePackageJson(outputDir, appIR)
writeNextConfig(outputDir)
writeTsConfig(outputDir)
writePostcssConfig(outputDir)
